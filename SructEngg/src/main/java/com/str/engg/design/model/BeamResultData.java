package com.str.engg.design.model;

public class BeamResultData {
	double x;
	double Moment;
	double Shear;
	double deflection;
	
	public double getX() {
		return x;
	}
	public void setX(double x) {
		this.x = x;
	}
	public double getMoment() {
		return Moment;
	}
	public void setMoment(double moment) {
		Moment = moment;
	}
	public double getShear() {
		return Shear;
	}
	public void setShear(double shear) {
		Shear = shear;
	}
	public double getDeflection() {
		return deflection;
	}
	public void setDeflection(double deflection) {
		this.deflection = deflection;
	}
	
	
	
}
